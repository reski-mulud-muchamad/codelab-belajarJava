package com.dicoding.javafundamental.operator;

public class OperatorAritmatika {

    public static void main(String[] args) {
        System.out.println("Operasi penjumlahan");
        int hasilPenjumlahan = 5 + 1;
        System.out.println("Hasil 5 + 1 = " + hasilPenjumlahan);
        System.out.println();

        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 5 - 1;
        System.out.println("Hasil 5 - 1 = " + hasilPengurangan);
        System.out.println();

        System.out.println("Operasi Pengalian");
        int hasilPengalian = 5 * 5;
        System.out.println("Hasil 5 x 5 = " + hasilPengalian);
        System.out.println();

        System.out.println("Operasi pembagian");
        int hasilPembagian = 6 / 3;
        System.out.println("Hasil 6 / 3 = " + hasilPembagian);
        System.out.println();

        System.out.println("Operasi Habis bagi");
        int hasilSisa = 8 % 4;
        System.out.println("Hasil 8 % 4 = " + hasilSisa);
        int hasilSisaLain = 9 % 4;
        System.out.println("Hasil 9 % 4 = " + hasilSisaLain);
        System.out.println();
    }

}
